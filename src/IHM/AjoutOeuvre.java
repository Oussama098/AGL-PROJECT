    private void ajouter(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ajouter

        try {
            oeuvre oeuvre = getOeuvreInfos();
            oeuvreController.add(oeuvre);
            ((DefaultTableModel) GestionOeuvres.tableListeOeuvre.getModel())
                    .addRow(
                            new Object[]{oeuvre.getId(),oeuvre.getISBN(), oeuvre.getTitre(), oeuvre.getAuteur(),
                                oeuvre.getCategorie()});
            
            clearField();
            JOptionPane.showMessageDialog(null,"Oeuvre ajout√©e avec succ√®s");
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_ajouter

    private oeuvre getOeuvreInfos() {
        //int identifiant = Integer.parseInt(identifiantField.getText());
        String ISBN = isbnfild.getText();
        String titre = titreField.getText();
        String auteur = auteurField.getText();
        String categorie = categorieField.getText();
        return new oeuvre(ISBN, titre, auteur, categorie);
    }
